    /**
     * Store a newly created resource in storage.
     *
     * @param  Request  $request
     * @return Response
     */
    public function store(Request $request) {
        $data = Paragraph::find(Input::get("id"));
        if (!$data) {
            $data = new Paragraph;
            $data->type = 'P';
            $data->uid_create = 1;
            $data->uid_edit = 1;
        }
        $data->title = Input::get("title");
        $data->content = Input::get("content");
        $data->save();
        return $data->id;
    }

